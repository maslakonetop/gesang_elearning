<div id="isi">
Tes Soal Online merupakan bagian dari sistem pembelajaran digital atau <b>e-learning</b>. Dimana kita diharapkan untuk bisa belajar secara mandiri tanpa terbatas oleh waktu dan tempat. Semoga melalui E-Learning dan Sistem Tes Soal Online ini, dapat tercapainya proses pembelajaran yang efektif dan efisien.<br /> Salam Hangat.<br /><br />
<h2>Tata Cara Pelaksanaan Tes Soal Online</h2>
<ul>
<li class="li-class">Isi user ID dan password dengan benar. Jika belum tahu atau tidak berhasil, tanyakan pada pengawas, jika anda telah mengisi dengan benar maka nama ada akan muncul di layar sebelah kiri (side bar).</li>
<li class="li-class">Pilih soal sesuai petunjuk guru atau pengawas, isi password soal dengan benar.</li>
<li class="li-class">Selama mengerjakan soal tidak perlu menggunakan keyboard. Penekanan ENTER akan dianggap anda telah menyelesaikan soal.</li>
<li class="li-class">Kerjakan soal secara hati-hati, klik pada lingkaran di sebelah kiri pilihan yang kamu anggap benar.</li>
<li class="li-class">Perhatikan waktu tersisa di atas jika mencapai 0 menit, soal akan ditutup dan dikoreksi secara otomatis.</li>
<li class="li-class">Jika anda sudah menyelesaikan soal sebelum waktu habis, klik tombol selesai pada lembar soal bagian paling bawah.</li>


